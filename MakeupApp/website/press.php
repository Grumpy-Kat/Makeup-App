				$("#imgsCarousel").on(
					"slide.bs.carousel",
					function(e) {
						var $e = $(e.relatedTarget);
						var index = $e.index();
						var itemsPerSlide = 4;
						var totalItems = $(".item").length;
						if(index >= totalItems - (itemsPerSlide - 1)) {
							var items = itemsPerSlide - (totalItems - index);
							for(var i = 0; i < items; i++) {
								// append slides to end
								if(e.direction == "left") {
									$(".item").eq(i).appendTo(".carousel-inner");
								}
								else {
									$(".item").eq(0).appendTo(".carousel-inner");
								}
							}
						}
					}
