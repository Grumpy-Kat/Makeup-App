			<h3>Features</h3>
			<p>Features:</p>
			<ul>
				<li>Easily import a picture of your eyeshadow palettes, divide up the shades, and GlamKit will find the color and finish of each shade.</li>
				<li>Add more details to each shade, such as tags, rating, price, and weight, to make it easier to remember how a shade performs, which shades you love, and find the perfect shade for your look.</li>
				<li>See all your eyeshadows in one scrollable list, so you no longer have to open all your palettes as you create a look.</li>
				<li>Sort the shades however you want, such as by color, finish, palette, favorite shades, darkest shades, or lightest shades.</li>
				<li>Double click on any shade you want to add to Today's Look.</li>
				<li>Save and name your stunning looks to remember and recreate another day. It also makes packing your travel bag easier because you no longer have to worry about leaving an important palette at home.</li>
				<li>Find your precise color using a color wheel to help if you're copying a face chart or have a specific color in mind.</li>
				<li>Compare other palettes to your collection.</li>
				<li>If you're shopping, you can just upload a picture of a palette you're considering. GlamKit will show you all the similar shades in your collection. It's a great way to save some money and avoid buying an almost identical shade for the hundredth time.</li>
				<li>If you're trying to recreate a look that you saw on Instagram or Youtube, just upload a picture of a palette that the original artist used. You can easily find dupes in your collection. It saves you time because you no longer have to spend time comparing swatches or Googling dupes that you might not even own.</li>
			</ul>
			<p>Future features: </p>
			<ul>
				<li>Filtering swatch lists based on color, finish, rating, tags, and more to make it easier to find that perfect shade in your collection.</li>
				<li>Making swatch lists searchable to make it even easier to find the shade you have in mind.</li>
				<li>Adding photos of swatches to each shade, including in different lights, in case the shadows look different than in the pan.</li>
				<li>Adding photos of your looks and dates that you wore a look.</li>
				<li>Adding notes and estimates of the time it will take to do a look, to make it easier to plan your mornings.</li>
				<li>Sharing your looks and collection with friends, so you can show off your gorgeous makeup.</li>
				<li>Improving finish detection.</li>
				<li>Adding features for iridescent, multichrome, and duochrome eyeshadows to support your full collection.</li>
				<li>Face, lips, sfx, and other product sections to help you create a full face look.</li>
			</ul>
			<p>If you have an idea for a feature not listed here, you can <a href=\"contact.php?type=feature\">request it here.</a></p>
